# 4. Пользователь вводит целое положительное число. Найдите самую большую цифру в числе.
# Для решения используйте цикл while и арифметические операции.

user_integer = input("Укажите произвольное число >>> ")
user_int_index: int = len(user_integer) - 1
user_digits = [user_digits for user_digits in user_integer]

print("Число пользователя: ", user_integer, ". Числовая последовательность", user_digits)

a = user_digits[user_int_index]

while True:
    if user_int_index < 0:
        break

    if a > user_digits[(user_int_index - 1)]:
        user_int_index -= 1
        continue
    else:
        a = user_digits[(user_int_index - 1)]
        user_int_index -= 1
        continue

print(a)
